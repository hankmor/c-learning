#include <stdio.h>

int main() {
    int a = 8;
    int b = 7;
    printf("\n a>b=%d", a > b); // 1
    printf("\n a>=b=%d", a >= b); // 1
    printf("\n a<b=%d", a < b); //0
    printf("\n a<=b=%d", a <= b);//0
    printf("\n a==b=%d", a == b);//0
    printf("\n a!=b=%d", a != b);//1
}
